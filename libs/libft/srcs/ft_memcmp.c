#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t	i;
	t_byte	*tmp_s1;
	t_byte	*tmp_s2;

	i = 0;
	tmp_s1 = (t_byte *)s1;
	tmp_s2 = (t_byte *)s2;
	while (i < n)
	{
		if (tmp_s1[i] != tmp_s2[i])
			return (tmp_s1[i] - tmp_s2[i]);
		i++;
	}
	return (0);
}
